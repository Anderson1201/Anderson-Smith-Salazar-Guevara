#Calcular el volumen de un cilindro
volumen, pi, radio, generatriz=0.0, 0.0, 0.0, 0.0

#Asignacion de valores
pi=3.14
radio=5
generatriz=7.2

#Calculo
volumen=(pi*radio*radio*generatriz)

#Mostrar valores
print ("pi", pi)
print ("radio", radio)
print ("generatriz", generatriz)
print ("volumen del cilindro", volumen)
