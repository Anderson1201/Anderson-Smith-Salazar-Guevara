#Calcular la suma de angulos internos de un triangulo
suma_angulos, angulo1, angulo2, angulo3=0.0, 0.0, 0.0, 0.0

#Asignacion de valores
angulo1=60
angulo2=80
angulo3=40

#Calculo
suma_angulos=(angulo1 + angulo2 + angulo3)

#Mostrar valores
print ("angulo α", angulo1)
print ("angulo β", angulo2)
print ("angulo ¥", angulo3)
print ("suma de angulos internos", suma_angulos)

